public class IfElseExample {
    public static void main(String[] args) {
        int score = 85;
        
        if(score>=90) {
            System.out.println("up 90");
            System.out.println("grade : A");
        }else {
            System.out.println("down 90");
            System.out.println("grade : B");
        }
    }
}
